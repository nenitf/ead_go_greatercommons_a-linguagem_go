package main

import "fmt"

func main() {
	// relacionais
	fmt.Println("Relacionais ↓")
	fmt.Println(4 < 10)
	fmt.Println(4 <= 4)
	fmt.Println(4 > 10)
	fmt.Println(4 >= 4)

	// lógicos
	fmt.Println("Logicos ↓")
	fmt.Println(true && false)
	fmt.Println(true || false)
	fmt.Println("sera" == "seraaa")
	fmt.Println("sera?" != "sera?")
}
